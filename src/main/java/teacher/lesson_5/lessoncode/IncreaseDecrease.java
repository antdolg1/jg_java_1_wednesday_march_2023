package teacher.lesson_5.lessoncode;

public class IncreaseDecrease {

    public static void main(String[] args) {

        int number = 0;

        System.out.println("number value is: " + number);

        number = number + 1; //same as number++;

        System.out.println("number value is: " + number);

        number++; //same as number + 1;

        System.out.println("number value is: " + number);

        number++;
        number++;
        number++;
        number++;
        number++;

        System.out.println("number value is: " + number);

        number--;
        number--;
        number--;

        System.out.println("number value is: " + number);
    }
}
