package teacher.lesson_10.lessoncode;

public class Converter {

    public void convert(int value) {
        System.out.println("Converting int: " + value);
    }

    public void convert(double value) {
        System.out.println("Converting double: " + value);
    }

    public void convert(String value) {
        System.out.println("Converting String: " + value);
    }

    public void convert(int[] values) {
        System.out.println("Converting int[]: " + values);
    }

}
