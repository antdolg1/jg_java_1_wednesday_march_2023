package teacher.lesson_6.lessoncode.kahoot;

public class Question14 {
    public static void main(String[] args) {

        int y = 0;
        for (int i = 0; i < 10; ++i) {
            y += i; //same as y = y + i;
            System.out.println("y value on iteration " + i + " is: " + y);
        }
        System.out.println(y);

    }
}
