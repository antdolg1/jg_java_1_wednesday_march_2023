public class Lesson2 {

    public static void main(String[] args) {

        int A = 10;
        int B = 20;
        int C = 30;

        int sum = A + B + C;
        int sub = C - B;
        int div = C / A;
        int mul = A * B * C;

        System.out.println("Result of A and B and C is " + sum);
        System.out.println("Result of C and B is " + sub);
        System.out.println("Result of C and A is " + div);
        System.out.println("Result of A and B and C is " + mul);


    }
}
