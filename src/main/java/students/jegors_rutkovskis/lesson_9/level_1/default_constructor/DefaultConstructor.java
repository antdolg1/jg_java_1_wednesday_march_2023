package students.jegors_rutkovskis.lesson_9.level_1.default_constructor;

class DefaultConstructor {
    private int number;
    // явно объявляем конструктор
    public DefaultConstructor() {}

    public int getNumber() {
        return this.number;
    }
    public void setNumber(int number) {
        this.number = number;
    }
}
